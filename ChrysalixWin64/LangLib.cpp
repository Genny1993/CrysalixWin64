void LangLib::setLang(LANGUAGES lang) {
	current_lang = lang;
}

std::wstring LangLib::getTrans(std::wstring str) {
	try {
		return library[current_lang].at(str);
	}
	catch (std::exception& err)
	{
		std::string temp = err.what();
		return str;
	}
}

